class FavoriteRide {
  final String name;
  final double rating;
  final bool isPremium;
  final String departure;
  final String arrival;
  final String date;
  final String time;
  final int seats;
  final double price;

  FavoriteRide({
    required this.name,
    required this.rating,
    required this.isPremium,
    required this.departure,
    required this.arrival,
    required this.date,
    required this.time,
    required this.seats,
    required this.price,
  });
}
